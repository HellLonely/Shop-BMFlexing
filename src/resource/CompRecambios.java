    private void tipoRecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tipoRecActionPerformed
        String clases= (String) tipoRec.getSelectedItem();
        if (clases.equals("frenos")){
            rellenarFrenosComboBox();
        } else if (clases.equals("cuadro")){
            rellenarCuadroComboBox();
        } else if (clases.equals("ruedas")){
            rellenarRuedasComboBox();
        } else if (clases.equals("horquilla")){
            rellenarHorquillaComboBox();
        } else if (clases.equals("shock")){
            rellenarShockComboBox();
        }
    }//GEN-LAST:event_tipoRecActionPerformed

    private void cantidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cantidadActionPerformed
        int cantidadRec= Integer.parseInt(cantidad.getText());
        int precio = DAO.actualizarPrecios((String) nombreObjeto.getSelectedItem());
        int finalP= precio * cantidadRec;
        String setPrecio= Integer.toString(finalP);
        precioFinal.setText(setPrecio);
    }//GEN-LAST:event_cantidadActionPerformed

    private void nombreObjetoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nombreObjetoActionPerformed
        int cantidadRec= Integer.parseInt(cantidad.getText());
        int precio = DAO.actualizarPrecios((String) nombreObjeto.getSelectedItem());
        int finalP= precio * cantidadRec;
        String setPrecio= Integer.toString(finalP);
        precioFinal.setText(setPrecio);
    }//GEN-LAST:event_nombreObjetoActionPerformed

    private void ComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComprarActionPerformed
        String transformar= precioFinal.getText();
        int precioBici= Integer.parseInt(transformar);
        factura.setPrecioTotal(precioBici);
        factura.setIdCliente(UserId);
        factura.setNome("Recambio");
        System.out.println("ID CLIENTE" + UserId);
        DAO.insertarFactura(factura.getPrecioTotal(), UserId, factura.getIdEmpleado(), factura.getTipo() ,factura.getTipo());
    }//GEN-LAST:event_ComprarActionPerformed

